#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*cpy;
	int			size;
	int			i;

	size = ft_strlen(s) + 1;
	cpy = (char *) malloc(size * sizeof(char));
	if (!(cpy))
		return (0);
	i = 0;
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
