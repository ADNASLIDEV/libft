#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	char				*tmp;
	unsigned int		i;

	tmp = (char *)s;
	i = 0;
	while (i < n)
		tmp[i++] = c;
	return (s);
}
